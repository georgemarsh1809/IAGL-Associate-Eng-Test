const repository = require('../../src/repository/todo');

describe('TODO repository', () => {
    it('should return the todo list', async () => {
        const expected = {
            todos: [
                {
                    task: 'This is a todo example',
                },
            ],
        };
        const actual = await repository.getTodos();
        expect(actual).toEqual(expected);
    });
});
